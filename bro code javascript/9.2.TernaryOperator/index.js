// ternary operator = a shortcut to if{} and else{} statements
//                    helps to assign a variable based on condition
// Formula = condition ? codeIfTrue : codeIfFalse;

let age = 21;
let message = age >= 18 ? "You're an adult" : "You are a minor";
console.log(message);

let time = 9;
let greeting = time < 12 ? "Good morning!" : "Good afternoon!";
console.log(greeting);

let isStudent = false;
let pesan = isStudent ? "You are a student" : "You are not a student";
console.log(pesan)